"use strict";

/**
 * Optional developer utility: fetches ALL themes from Strapi and regenerates
 * .storybook/themes.ts so Storybook's toolbar includes real production themes.
 *
 * Usage:
 *   NEXT_PUBLIC_STRAPI_URL=http://localhost:1337 node scripts/generate-storybook-themes.js
 *
 * The output file is committed to the repo so Storybook works offline.
 * Re-run this script whenever themes change in Strapi.
 *
 * This script reuses the same THEME_VARIABLE_MAP and themeToCSS logic from
 * lib/theme.js to ensure CSS output exactly matches production behaviour.
 */

const fs = require("fs");
const path = require("path");

const STRAPI_URL = process.env.NEXT_PUBLIC_STRAPI_URL;
const OUTPUT_PATH = path.join(__dirname, "..", ".storybook", "themes.ts");

// Replicate the variable map from lib/theme.js (CJS-compatible copy)
const THEME_VARIABLE_MAP = [
  ["colors.bg_base", "--color-bg-base"],
  ["colors.bg_base", "--color-background"],
  ["colors.bg_base", "--color-primary-foreground"],
  ["colors.bg_base", "--color-accent-foreground"],
  ["colors.bg_raised", "--color-bg-raised"],
  ["colors.bg_raised", "--color-card"],
  ["colors.bg_raised", "--color-popover"],
  ["colors.bg_raised", "--color-input"],
  ["colors.bg_inset", "--color-bg-inset"],
  ["colors.bg_inset", "--color-muted"],
  ["colors.text_muted", "--color-text-muted"],
  ["colors.text_body", "--color-text-body"],
  ["colors.text_body", "--color-muted-foreground"],
  ["colors.text_body", "--color-card-foreground"],
  ["colors.text_heading", "--color-text-heading"],
  ["colors.text_heading", "--color-foreground"],
  ["colors.text_heading", "--color-secondary-foreground"],
  ["colors.text_heading", "--color-destructive-foreground"],
  ["colors.text_heading", "--color-popover-foreground"],
  ["colors.accent", "--color-accent"],
  ["colors.accent", "--color-primary"],
  ["colors.accent", "--color-ring"],
  ["colors.interactive", "--color-interactive"],
  ["colors.neutral", "--color-neutral"],
  ["colors.neutral", "--color-secondary"],
  ["colors.info", "--color-info"],
  ["colors.danger", "--color-danger"],
  ["colors.danger", "--color-destructive"],
  ["colors.success", "--color-success"],
];

function get(obj, path) {
  return path.split(".").reduce((curr, key) => curr?.[key], obj);
}

function themeToCSS(theme) {
  if (!theme) return "";
  const declarations = [];
  for (const [path, cssVar] of THEME_VARIABLE_MAP) {
    const value = get(theme, path);
    if (value !== null && value !== undefined && value !== "") {
      declarations.push(`  ${cssVar}: ${value};`);
    }
  }
  if (declarations.length === 0) return "";
  return `:root {\n${declarations.join("\n")}\n}`;
}

async function fetchThemes() {
  const url = `${STRAPI_URL}/api/themes?populate[colors]=*&populate[typography]=*&populate[spacing]=*&populate[layout]=*`;
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`Strapi responded with ${res.status} for ${url}`);
  }
  const json = await res.json();
  return (json.data ?? []).map((entry) => entry.attributes ?? entry);
}

function generateThemesTS(themes) {
  const entries = themes.map((theme) => {
    const css = themeToCSS(theme);
    // Escape backticks in the CSS string (shouldn't occur but be safe)
    const safeCss = css.replace(/`/g, "\\`");
    return `  { id: ${JSON.stringify(theme.name.toLowerCase().replace(/\s+/g, "-"))}, name: ${JSON.stringify(theme.name)}, css: \`${safeCss}\` }`;
  });

  return `// AUTO-GENERATED by scripts/generate-storybook-themes.js
// Do not edit manually — run the script to regenerate from Strapi.
// Last generated: ${new Date().toISOString()}

export interface StorybookTheme {
  id: string;
  name: string;
  /** Raw CSS string injected into document.head as a <style> tag. */
  css: string;
}

export const THEMES: StorybookTheme[] = [
  { id: "default", name: "Default (Void)", css: \`\` },
${entries.join(",\n")},
];
`;
}

(async () => {
  console.log(`Fetching themes from ${STRAPI_URL}...`);
  try {
    const themes = await fetchThemes();
    if (themes.length === 0) {
      console.warn("No themes found in Strapi. Keeping existing themes.ts.");
      process.exit(0);
    }
    console.log(
      `Found ${themes.length} theme(s): ${themes.map((t) => t.name).join(", ")}`,
    );
    const output = generateThemesTS(themes);
    fs.writeFileSync(OUTPUT_PATH, output, "utf8");
    console.log(`Written to ${OUTPUT_PATH}`);
  } catch (err) {
    console.error("Failed to fetch themes:", err.message);
    console.error(
      "Is Strapi running? Set NEXT_PUBLIC_STRAPI_URL to the correct URL.",
    );
    process.exit(1);
  }
})();
